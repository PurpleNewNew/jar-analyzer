package me.n1ar4.jar.analyzer.entity;

public class LocalVarEntity {
    private String className;
    private String methodName;
    private String methodDesc;
    private Integer varIndex;
    private String varName;
    private String varDesc;
    private String varSignature;
    private Integer startLine;
    private Integer endLine;
    private Integer jarId;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }

    public String getMethodDesc() {
        return methodDesc;
    }

    public void setMethodDesc(String methodDesc) {
        this.methodDesc = methodDesc;
    }

    public Integer getVarIndex() {
        return varIndex;
    }

    public void setVarIndex(Integer varIndex) {
        this.varIndex = varIndex;
    }

    public String getVarName() {
        return varName;
    }

    public void setVarName(String varName) {
        this.varName = varName;
    }

    public String getVarDesc() {
        return varDesc;
    }

    public void setVarDesc(String varDesc) {
        this.varDesc = varDesc;
    }

    public String getVarSignature() {
        return varSignature;
    }

    public void setVarSignature(String varSignature) {
        this.varSignature = varSignature;
    }

    public Integer getStartLine() {
        return startLine;
    }

    public void setStartLine(Integer startLine) {
        this.startLine = startLine;
    }

    public Integer getEndLine() {
        return endLine;
    }

    public void setEndLine(Integer endLine) {
        this.endLine = endLine;
    }

    public Integer getJarId() {
        return jarId;
    }

    public void setJarId(Integer jarId) {
        this.jarId = jarId;
    }
}
